package tree;

public class Symboll {
    private int cod;
    private String id;
    private int direccion;

    public Symboll() {
        this.cod = -1;
        this.id = "";
    }

    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getDireccion() {
        return direccion;
    }

    public void setDireccion(int direccion) {
        this.direccion = direccion;
    }
    
    
    
    
}
